package sub6;

public class ProductVO {
	private int pordNo;
	private String pordName;
	private int stock;
	private int price;
	private String company;
	public int getPordNo() {
		return pordNo;
	}
	public void setPordNo(int pordNo) {
		this.pordNo = pordNo;
	}
	public String getPordName() {
		return pordName;
	}
	public void setPordName(String pordName) {
		this.pordName = pordName;
	}
	public int getStock() {
		return stock;
	}
	public void setStock(int stock) {
		this.stock = stock;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	
}
